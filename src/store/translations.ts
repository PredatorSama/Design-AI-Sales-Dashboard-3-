/**
 * Helper function to get translated text
 * @param key - Translation key (e.g., 'nav.dashboard')
 * @param language - Language code (e.g., 'EN', 'ES')
 * @param fallback - Fallback text if key not found
 * @returns Translated text or fallback
 */
export function t(key: string, language: string = 'EN', fallback: string = key): string {
  const lang = language as keyof typeof translations;
  if (!translations[lang]) {
    return fallback;
  }
  const translationKey = key as keyof typeof translations[typeof lang];
  return (translations[lang][translationKey] as string) || fallback;
}
